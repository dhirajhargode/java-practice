package com.lf.literals;

public class EscapeCharacher {
	
	public static void main(String[] args) {
		
		System.out.println("new line :  Dhiraj\nHargode");
		System.out.println("horizontal tab :Dhiraj\tHargode");
		System.out.println("Carriage : Dhiraj\rHargode");
		System.out.println("form feed : Dhiraj\fHargode");
		System.out.println("back space char :Dhiraj\bHargode");
		System.out.println("Single quote : \'Dhiraj\' Hargode");
		System.out.println("BackSpace : Dhiraj \\ Hargode");
		System.out.println("double Quote :: \"Dhiraj\" Hargode");
		
	}

}
