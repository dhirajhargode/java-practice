
public class ThreadSleep {

	public static void main(String[] args) throws InterruptedException{
		System.out.println("m");
		Thread.sleep(3000);
		System.out.println("e");
		Thread.sleep(3000);
		System.out.println("g");
		Thread.sleep(3000);
		System.out.println("a");
		Thread.sleep(3000);
		
	}
}
