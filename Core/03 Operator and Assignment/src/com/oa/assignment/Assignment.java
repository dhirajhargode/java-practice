package com.oa.assignment;

public class Assignment {

	public static void main(String[] args) {
		
		//simple assignment
		int x=10;
		
		//chain assignment
		int a,b,c,d;
		a=b=c=d=20;
		System.out.println(a+" "+b+" "+c+" "+d);
		
		//int a=b=c=d=20; //b c d not declare only 1st variable declare 
		int m,n,o;
		int l=m=n=o=30;  // m n o are declared already 
		
		
		//compound assignment 
		int a1=10;
		a1+=20;
		System.out.println(a1);
		
		
		// >>= sign bit right shift
		// >>>= unsigned bit 
			
	}
}
